package com.tkx.driver;

public class GeoFence {

    private String result;
    private String message;
    private String geofence_queue_text;
    private String geofence_queue_color;

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getGeofence_queue_text() {
        return geofence_queue_text;
    }

    public void setGeofence_queue_text(String geofence_queue_text) {
        this.geofence_queue_text = geofence_queue_text;
    }

    public String getGeofence_queue_color() {
        return geofence_queue_color;
    }

    public void setGeofence_queue_color(String geofence_queue_color) {
        this.geofence_queue_color = geofence_queue_color;
    }
}
